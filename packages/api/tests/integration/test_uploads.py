def test_cross_tenant_get_returns_404(client: tuple[TestClient, str, str, Path]) -> None:
    c, uid_a, uid_b, _ws = client
    pid = _create_persona(c, uid_a)
    # User A uploads.
    resp = c.post(
        f"/v1/personas/{pid}/uploads",
        files={"file": ("tiny.png", _TINY_PNG, "image/png")},
        headers=_auth(uid_a),
    )
    assert resp.status_code == 201
    ref = resp.json()["workspace_path"]
    # User B GETs A's ref on A's persona → pre-flight RLS rejects (404).
    resp = c.get(f"/v1/personas/{pid}/uploads/{ref}", headers=_auth(uid_b))
    assert resp.status_code == 404


def test_cross_tenant_post_returns_404(client: tuple[TestClient, str, str, Path]) -> None:
    c, uid_a, uid_b, _ws = client
    pid = _create_persona(c, uid_a)
    # User B tries to POST to A's persona id → pre-flight RLS rejects (404).
    resp = c.post(
        f"/v1/personas/{pid}/uploads",
        files={"file": ("tiny.png", _TINY_PNG, "image/png")},
        headers=_auth(uid_b),
    )
    assert resp.status_code == 404


def test_traversal_attempts_blocked(client: tuple[TestClient, str, str, Path]) -> None:
    c, uid_a, _uid_b, _ws = client
    pid = _create_persona(c, uid_a)
    # Traversal ref ("..") rejects as 404 via the sandbox resolver.
    resp = c.get(
        f"/v1/personas/{pid}/uploads/uploads/..%2F..%2Fetc%2Fpasswd",
        headers=_auth(uid_a),
    )
    assert resp.status_code == 404


def test_rate_limit_returns_429(client: tuple[TestClient, str, str, Path]) -> None:
    c, uid_a, _uid_b, _ws = client
    pid = _create_persona(c, uid_a)
    # Override the limiter to a tiny limit so the 4th call trips it.
    c.app.state.rate_limiter = RateLimiter(
        InMemoryRateLimitStore(), default_limit=3, per_endpoint={}
    )
    for _ in range(3):
        resp = c.post(
            f"/v1/personas/{pid}/uploads",
            files={"file": ("t.png", _TINY_PNG, "image/png")},
            headers=_auth(uid_a),
        )
        assert resp.status_code == 201, resp.text
    # 4th call → 429 + the standard rate-limit headers.
    resp = c.post(
        f"/v1/personas/{pid}/uploads",
        files={"file": ("t.png", _TINY_PNG, "image/png")},
        headers=_auth(uid_a),
    )
    assert resp.status_code == 429, resp.text
    assert resp.headers["X-RateLimit-Limit"] == "3"
    assert resp.headers["X-RateLimit-Remaining"] == "0"
    assert "X-RateLimit-Reset" in resp.headers
    assert "Retry-After" in resp.headers
